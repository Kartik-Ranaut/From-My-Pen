<div className="text-justify">Vande Mataram गीत बंकिम चंद्र चटर्जी द्वारा भारत माता की स्तुति मैं लिखा गया है। भारत के राष्ट्रीय गीत के बोल के रूप में &lsquo;Vande Mataram&rsquo; की पहली दो पंक्तियों को 1950 में भारत की संविधान सभा द्वारा अपनाया गया था।<br />Vande Mataram का मूल संस्करण बंकिम चंद्र चट्टोपाध्याय/चटर्जी द्वारा लिखा गया था।बंकिम चंद्र चट्टोपाध्याय को भारतीय और बंगाली इतिहास की घटनाओं, विशेषकर 1857 के विद्रोह और संन्यासी विद्रोह, जो एक सदी पहले हुआ था, में गहरी रुचि थी। 1876 में एक सरकारी अधिकारी के रूप में उनके कार्यकाल के दौरान, औपनिवेशिक प्रशासन ब्रिटिश भारत के राष्ट्रगान के रूप में &ldquo;गॉड सेव द क्वीन&rdquo; को बढ़ावा देने की कोशिश कर रहा था।कहने की जरूरत नहीं है कि भारतीय राष्ट्रवादियों का विशाल बहुमत इस तरह के कदम के खिलाफ था। इस प्रकार बंकिम चंद्र चट्टोपाध्याय ने एक ऐसी कविता लिखी जो भारत की मूल पहचान को बरकरार रखते हुए इसके समृद्ध सांस्कृतिक इतिहास को समाहित करेगी।चट्टोपाध्याय ने संस्कृत और बंगाली के शब्दों का उपयोग करते हुए एक सहज सत्र में कविता लिखी, हालाँकि यह कविता मुख्यतः संस्कृत मैं लिखी गई है परन्तु कुछ शब्द बांग्ला भाषा के भी हैं।यह कविता 1882 में चट्टोपाध्याय की पुस्तक आनंदमठ (बंगाली में उच्चारित अनंदोमोह) में प्रकाशित हुई थी, जो संन्यासी विद्रोह की घटनाओं पर आधारित है।इस कविता के लिखे जाने के तुरंत बाद जदुनाथ भट्टाचार्य को इसकी धुन तैयार करने के लिए कहा गया था। आधुनिक Vande Mataram&nbsp;रचना,&nbsp;जैसा कि आज सुना जाता है,&nbsp;का श्रेय गंधर्व महाविद्यालय और अखिल भारतीय गंधर्व महाविद्यालय मंडल के संस्थापक वी.&nbsp;डी.&nbsp;पलुस्कर को दिया जाता है।&nbsp;24 जनवरी 1950 को भारत की संविधान सभा ने &ldquo;Vande Mataram&rdquo;&nbsp;को राष्ट्रीय गीत के रूप में अपनाया। इस अवसर पर,&nbsp;भारत के पहले राष्ट्रपति,&nbsp;राजेंद्र प्रसाद ने कहा कि इस गीत को भारत के राष्ट्रीय गान, &ldquo;जन गण मन&rdquo;&nbsp;के समान सम्मान दिया जाना चाहिए। हालाँकि,&nbsp;भारत के संविधान में&nbsp;&ldquo;राष्ट्रीय गीत&rdquo;&nbsp;का कोई उल्लेख नहीं है।राष्ट्रीय गीत मैं सम्मलित वन्दे मातरम की पंक्तियाँ पूर्णतः संस्कृत मैं हैं। भारतीय राष्ट्रीय कांग्रेस का 1896 का अधिवेशन सर्वप्रथम राजनीतिक आयोजन है जब राष्ट्रीय गीत गाया गया। भारत का राष्ट्रीय गीत प्रथम बार इसी अवसर पर रवीन्द्रनाथ टैगोर सत्र में गाया गया था।</div>
